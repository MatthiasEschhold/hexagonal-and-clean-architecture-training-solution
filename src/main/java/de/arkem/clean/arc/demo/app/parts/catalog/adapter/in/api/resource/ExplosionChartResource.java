package de.arkem.clean.arc.demo.app.parts.catalog.adapter.in.api.resource;

public class ExplosionChartResource {
    private int explosionChartId;
    private String name;
    private String description;

    public int getExplosionChartId() {
        return explosionChartId;
    }

    public void setExplosionChartId(int explosionChartId) {
        this.explosionChartId = explosionChartId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }
}
